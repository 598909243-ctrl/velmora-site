const imagePath = (path) => `${import.meta.env.BASE_URL}${path}`

const makeProduct = ({
  slug,
  name,
  category,
  tagline,
  image,
  images,
  colors,
  featured = false,
  availability = 'custom',
  price,
  useCases = ['Retail assortment', 'Wholesale sourcing'],
  customOptions = ['Logo print', 'Fabric color', 'Sleeve packaging'],
  badges,
  stockType,
  diameter = '105 cm',
  weight = '480 g',
  frame = 'Fiberglass and steel',
  opening = 'Automatic open',
  moq = '100 pcs for customization',
  leadTime = '25–35 days after sample approval',
  specifications,
  description,
  features,
}) => ({
  slug,
  name,
  category,
  tagline,
  description: description || `${tagline} Designed as a flexible demonstration model for global retail, wholesale, and branded programs.`,
  features: features || ['Water-repellent 190T pongee', 'Wind-flex frame', 'Custom color and logo options'],
  specifications: specifications || {
    Diameter: diameter,
    Weight: weight,
    Frame: frame,
    Fabric: '190T pongee',
    Handle: 'Comfort-grip ABS',
    Opening: opening,
  },
  colors,
  images: images || [image],
  moq,
  leadTime,
  featured,
  availability,
  stockType: stockType || (availability === 'ready-stock' ? 'Ready-stock inquiry' : 'Custom program'),
  badges: badges || [
    availability === 'ready-stock' ? 'Ready-stock inquiry' : 'OEM ready',
    opening,
  ],
  useCases,
  customOptions,
  comparisonSpecs: {
    Diameter: diameter,
    MOQ: moq,
    LeadTime: '25–35 days',
  },
  price,
})

